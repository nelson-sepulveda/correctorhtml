    private void btnArchivoPCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnArchivoPCActionPerformed
        // TODO add your handling code here:
        try {

            BufferedReader entrada;
            String codigo = "";

            JFileChooser input = new JFileChooser();
            input.setMultiSelectionEnabled(false);
            input.setAcceptAllFileFilterUsed(false);
            FileNameExtensionFilter filter = new FileNameExtensionFilter("HTML files", "html", "htm");
            input.setFileFilter(filter);

            input.setAcceptAllFileFilterUsed(false);

            int result = input.showSaveDialog(this);

            if (result == JFileChooser.APPROVE_OPTION) {
                File f = input.getSelectedFile();
                entrada = new BufferedReader(new FileReader(f));
                while (entrada.ready()) {
                    codigo += entrada.readLine() + "\n";
                }
                this.txtEditor.setText(codigo);
            } else if (result == JFileChooser.CANCEL_OPTION) {
            }

        } catch (Exception ufps) {
            System.out.println(ufps.getMessage());
        }
    }//GEN-LAST:event_btnArchivoPCActionPerformed

