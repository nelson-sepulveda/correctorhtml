    private void btnValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidarActionPerformed
        // TODO add your handling code here:

        try {
            this.sistema.cargarCodigoFuente(this.txtEditor.getText());
            this.sistema.validarHTML();

            Pila<ErrorHTML> p;
            p = sistema.retornarPila();
            String datos[][] = new String[p.getSize()][2];

            int i = 0;
            while (!p.esVacio()) {
                ErrorHTML e = p.pop();
                datos[i][0] = e.getMyEtiqueta().getEtiqueta() + "";
                datos[i][1] = e.getMyError().getDescripcion() + "";
                i++;
                System.out.println(e.getMyEtiqueta().getEtiqueta() + " " + e.getMyError().getDescripcion());
            }
            if (datos.length >= 1) {
                DialogErroresHTML d = new DialogErroresHTML(datos);
                d.setVisible(true);
            } else {

                JOptionPane.showMessageDialog(this, "No se encontro ningun error en el archivo. \nProcede a guardar el archivo.");

                try {

                    JFileChooser input = new JFileChooser();
                    FileNameExtensionFilter filter = new FileNameExtensionFilter("HTML files", "html", "htm");
                    input.setFileFilter(filter);
                    input.setAcceptAllFileFilterUsed(false);
                    int result = input.showSaveDialog(this);
                    if (result == JFileChooser.APPROVE_OPTION) {
                        File f = input.getSelectedFile();

                        FileWriter fw = new FileWriter(f + ".html");
                        fw.write(this.txtEditor.getText());
                        fw.close();

                    } else if (result == JFileChooser.CANCEL_OPTION) {
                    }

                } catch (Exception ufps) {
                    System.out.println(ufps.getMessage());
                }
            }
        } catch (ExceptionUFPS ufps) {
            System.out.println(ufps);
        }

    }//GEN-LAST:event_btnValidarActionPerformed

