    /**
     * Metodo que permite cargar los respectivos items, para luego guardar los
     * nombres de las etiquetas
     *
     * @param c
     */
    private void cargarItems(JComboBox c) {
        this.comboNomEtiqueta.removeAllItems();
        int n = c.getSelectedIndex();
        String datos[];

        this.comboNomEtiqueta.addItem("Seleccione la Etiqueta...");
        for (Object dato : v) {
            datos = dato.toString().split(";");
            if (c.getItemAt(n).equals(datos[0]) && !c.getItemAt(n).equals("Seleccione el Tipo...")) {
                if (datos[1].equals("<html>")) {
                    this.comboNomEtiqueta.addItem("\t<html>");
                } else if (datos[1].equals("<h1> to <h6>")) {
                    for (int i = 1; i <= 6; i++) {
                        this.comboNomEtiqueta.addItem("<h" + i + ">");
                    }
                } else if (datos[1].equals("<!--...-->")) {
                } else {
                    this.comboNomEtiqueta.addItem(datos[1]);
                }

            }
        }
    }

    /**
     * Metodo que permite insertar las etiquetas en el editor
     *
     * @param etiqueta
     */
    private void insertarEtiqueta(String etiqueta) {

        String eti;
        String aux = etiqueta.substring(0, 1);
        String d = "";
        for (int i = 1; i < etiqueta.length(); i++) {
            d += etiqueta.charAt(i);
        }
        eti = aux + "/" + d;
        txtEditor.insert(etiqueta + "\n" + eti + "\n", txtEditor.getCaretPosition());
    }

